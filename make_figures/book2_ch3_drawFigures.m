%% Figures 9 and 10abc

figs = book2_ex3_3();

figure(figs(1));
utils.setPlotStyle(gca,{'widescreen','equal','tight'});
utils.exportPlot(figs(1), [prefix, '9']);
for idx = 2:numel(figs)
    utils.exportPlot(figs(idx), [prefix '10' char('a' + idx-2)], true);
