%% Figures 11 and 12
if force_recalc
    figs = book2_ex3_4();
    
    suffixes = {'11', '12'};
    for idx = 1:numel(figs)
        figure(figs(idx));
        utils.setPlotStyle(gca,{'widescreen','tight'});
